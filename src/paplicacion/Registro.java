    private void BG1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BG1ActionPerformed
        // Boton Guardar

        CRegistro objeto = new CRegistro();
        SeleccionQYF SQ = new SeleccionQYF();
        
        
        objeto.nombre = nombre.getText();
        objeto.apellido = apellido.getText();
        objeto.nickname = apodo.getText();
        objeto.correo = correo.getText();
        objeto.contra = contra.getText();
        
        if (objeto.nombre.equals("")  || objeto.apellido.equals("") || objeto.nickname.equals("")|| objeto.correo.equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan campos por llenar","Datos No Guardados",JOptionPane.PLAIN_MESSAGE, this.icono);
        }
        else{JOptionPane.showMessageDialog(null, "Datos guardados con exito", "Datos Guardados",JOptionPane.PLAIN_MESSAGE, this.icono );
        this.setVisible(false);
        SQ.setVisible(true);
        
        }
      
        try{
          PreparedStatement pps = cn.prepareStatement("INSERT INTO Usuario(Nickname,Email,contrasena) VALUES(?,?,?)");
             pps.setString(1, apodo.getText());
             pps.setString(2, correo.getText());
             pps.setString(3, contra.getText());
             pps.executeUpdate();
             JOptionPane.showMessageDialog(null,"Bien");
        }catch(SQLException ex){
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null,ex);
        }
     
    }//GEN-LAST:event_BG1ActionPerformed

   
    
    
    
    
